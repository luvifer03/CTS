public class oddNumber {
	
	void odd(int odd) {
		if(odd%2!=0) {
			System.out.println("The number is a odd number");
			System.out.println("============================================");
		}
		else {
			System.out.println("The number is not a odd number");
			System.out.println("============================================");
		}
	}

}
